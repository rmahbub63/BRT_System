    private void allBusinCombo(){
        String sql="select * from AllBus";
	// here * pointed on column of database called InformationData...
        try {
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            while (rs.next()) {                
                String name=rs.getString("BusName");
                SelectBus_RouteSet.addItem(name);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
	 finally{
            try {
                pst.close();
                rs.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }
    private void allTerminalinCombo(){
        String sql="select * from AllTerminal";
	// here * pointed on column of database called InformationData...
        try {
            
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            while (rs.next()) {                
                String name=rs.getString("Tname");
                From_RouteSet.addItem(name);
                T2_RouteSet.addItem(name);
                T3_RouteSet.addItem(name);
                T4_RouteSet.addItem(name);
                To_RouteSet.addItem(name);
                From_FareDefine.addItem(name);
                To_FareDefine.addItem(name);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
	 finally{
            try {
                pst.close();
                rs.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }
