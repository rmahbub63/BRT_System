    private void RouteSetOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RouteSetOKActionPerformed
        // TODO add your handling code here:
        String sql="Insert into RouteSet(BusName,Start,T2,T3,T4,End,Seat) values (?,?,?,?,?,?,?)";
        try {
            pst=conn.prepareStatement(sql);
            pst.setString(1, (String)SelectBus_RouteSet.getSelectedItem());
            pst.setString(2, (String)From_RouteSet.getSelectedItem());
            pst.setString(3, (String)T2_RouteSet.getSelectedItem());
            pst.setString(4, (String)T3_RouteSet.getSelectedItem());
            pst.setString(5, (String)T4_RouteSet.getSelectedItem());
            pst.setString(6, (String)To_RouteSet.getSelectedItem());
            pst.setString(7, TotalSeat_RouteSet.getText());
            pst.execute();
            JOptionPane.showMessageDialog(null, "Route have been set");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_RouteSetOKActionPerformed

